// Go program to illustrate the
// use of relational operators
package main

import "fmt"

func main() {
   p:= "abc"
   q:= 20.0

   // ‘=='(Equal To)
   result1:= p == q
   fmt.Println(result1)

   // ‘!='(Not Equal To)
   result2:= p != q
   fmt.Println(result2)

   // ‘<‘(Less Than)
   result3:= p < q
   fmt.Println(result3)

   // ‘>'(Greater Than)
   result4:= p > q
   fmt.Println(result4)

   // ‘>='(Greater Than Equal To)
   result5:= p >= q
   fmt.Println(result5)

   // ‘<='(Less Than Equal To)
   result6:= p <= q
   fmt.Println(result6)


}
